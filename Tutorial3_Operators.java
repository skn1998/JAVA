public class Tutorial3_Operators {
    public static void main(String[] args) {
        char b = 'b';
        System.out.println(b);
        b++;  // b = c
        System.out.println(b);
        b+=1; // b = d
        System.out.println(b);
        // b = b + 1; this will show error
        System.out.println(b - 'a');
    }
}
