public class Tutorial4_String {
    public static void main(String[] args) {
        String str = "SK Nishant"; // Creating an String Object

        String lower_name = str.toLowerCase();
        System.out.println(lower_name);

        String repeated_name = str.repeat(2);
        System.out.println(repeated_name);

        System.out.println(str.length());
    }
}
