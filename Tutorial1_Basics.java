public class Tutorial1_Basics {
    public static void main(String[] args) { // static keyword helps us to run a method without creating any object
        System.out.println("Hello World!"); //println -> after printing adds a new line
        System.out.print("Hello all!"); // print -> it doesn't add new line after printing
        // for C style printing we also have System.out.printf("");
    }
}
